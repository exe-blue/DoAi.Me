/**
 * Prepares release.env for the packaged app so resources/.env is populated.
 * Reads from repo root .env.local (or .env) and writes only needed keys to release.env.
 * Run before electron-builder so extraResources can copy release.env -> resources/.env
 */
const fs = require("fs");
const path = require("path");

const DESKTOP_DIR = path.join(__dirname, "..", "..");
const ROOT_DIR = path.join(DESKTOP_DIR, "..", "..");
const KEYS = ["SUPABASE_URL", "SUPABASE_ANON_KEY", "SUPABASE_SERVICE_ROLE_KEY", "XIAOWEI_WS_URL", "XIAOWEI_TOOLS_DIR"];

function loadEnvFile(filePath) {
  const out = {};
  if (!fs.existsSync(filePath)) return out;
  const content = fs.readFileSync(filePath, "utf8");
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if (val.startsWith('"') && val.endsWith('"')) val = val.slice(1, -1).replace(/\\"/g, '"');
    if (val.startsWith("'") && val.endsWith("'")) val = val.slice(1, -1).replace(/\\'/g, "'");
    out[key] = val;
  }
  return out;
}

const env = {
  ...loadEnvFile(path.join(ROOT_DIR, ".env.local")),
  ...loadEnvFile(path.join(ROOT_DIR, ".env")),
  ...loadEnvFile(path.join(DESKTOP_DIR, ".env.local")),
  ...loadEnvFile(path.join(DESKTOP_DIR, ".env")),
  ...process.env,
};

const lines = ["# Packaged app env (generated by prepare-packaged-env.js)"];
for (const key of KEYS) {
  const val = env[key];
  if (val !== undefined && val !== "") {
    lines.push(`${key}=${val}`);
  }
}

const outPath = path.join(DESKTOP_DIR, "release.env");
fs.writeFileSync(outPath, lines.join("\n") + "\n", "utf8");
console.log("Wrote", outPath, "with keys:", KEYS.filter((k) => env[k]).join(", ") || "(none from env)");
